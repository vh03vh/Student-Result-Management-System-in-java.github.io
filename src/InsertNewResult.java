    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
       String rollNo=jTextField1.getText();
       String algo=jTextField2.getText();
       String ana=jTextField3.getText();
       String si=jTextField4.getText();
       String logi=jTextField5.getText();
       String stm=jTextField6.getText();
       try{
         Class.forName("com.mysql.jdbc.Driver");
     Connection con;
        con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","");
      Statement st=con.createStatement();
      ResultSet rs=st.executeQuery("select *from student where rollNo='"+rollNo+"' ");
      if(rs.next()){
           
          st.executeUpdate("insert into result (rollNo,algo,ana,si,logi,stm) values('"+rollNo+"','"+algo+"','"+ana+"','"+si+"','"+logi+"','"+stm+"')");
          JOptionPane.showMessageDialog(null, "Successful Update");  
            setVisible(false);
         new InsertNewResult().setVisible(true);
       
      }
      else{
      JOptionPane.showMessageDialog(null, "the rollNo does not existe on database "); 
        setVisible(false);
         new InsertNewResult().setVisible(true);
      
      
      }
       
       
       }
       catch(Exception e){
         JOptionPane.showMessageDialog(null, "connection errur");  
         
       }
    }//GEN-LAST:event_jButton6ActionPerformed

